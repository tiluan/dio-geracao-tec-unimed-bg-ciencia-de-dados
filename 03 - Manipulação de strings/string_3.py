nome = "Curso Python DIO"

# Imprime o primeiro caractere da string nome.
print(nome[0])

# Imprime o último caractere da string nome.
print(nome[-1])

# Imprime os caracteres da string nome do início até o índice 8 (não incluindo o 9).
print(nome[:9])

# Imprime os caracteres da string nome do índice 10 até o final.
print(nome[10:])

# Imprime os caracteres da string nome do índice 10 até o índice 15 (não incluindo o 16).
print(nome[10:16])

# Imprime os caracteres da string nome do índice 10 até o índice 15 (não incluindo o 16), pulando de 2 em 2 caracteres.
print(nome[10:16:2])

# Imprime todos os caracteres da string nome.
print(nome[:])

# Imprime todos os caracteres da string nome, em ordem inversa.
print(nome[::-1])