# Declaração da lista de frutas
frutas = ["laranja", "maca", "uva"]
print(frutas)

# Reatribuição da lista de frutas vazia
frutas = []
print(frutas)

# Convertendo uma string em uma lista de caracteres
letras = list("python")
print(letras)

# Criando uma lista de números usando a função range()
numeros = list(range(10))
print(numeros)

# Declaração da lista de informações de um carro
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
