# Criação de um dicionário
dados = {"nome": "Guilherme", "idade": 28, "telefone": "3333-1234"}

# Exibição de elementos
print(dados["nome"])
print(dados["idade"])
print(dados["telefone"])

# Atualização de elementos
dados["nome"] = "Maria"
dados["idade"] = 18
dados["telefone"] = "9988-1781"

# Exibição do resultado
print(dados)